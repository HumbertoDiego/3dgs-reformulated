def storePly(path, xyz, rgb):
    # Define the dtype for the structured array
    dtype = [('x', 'f4'), ('y', 'f4'), ('z', 'f4'),
            ('nx', 'f4'), ('ny', 'f4'), ('nz', 'f4'),
            ('red', 'u1'), ('green', 'u1'), ('blue', 'u1')]
    
    normals = np.zeros_like(xyz)

    elements = np.empty(xyz.shape[0], dtype=dtype)
    attributes = np.concatenate((xyz, normals, rgb), axis=1)
    elements[:] = list(map(tuple, attributes))

    # Create the PlyData object and write to file
    vertex_element = PlyElement.describe(elements, 'vertex')
    ply_data = PlyData([vertex_element])
    ply_data.write(path)

def get_point_clouds_colmap(path):
    ply_path = os.path.join(path, "sparse/points3D.ply")
    bin_path = os.path.join(path, "sparse/points3D.bin")
    txt_path = os.path.join(path, "sparse/points3D.txt")
    if not os.path.exists(ply_path):
        print("Converting point3d.bin to .ply, will happen only the first time you open the scene.")
        try:
            xyz, rgb, _ = read_points3D_binary(bin_path)
        except:
            xyz, rgb, _ = read_points3D_text(txt_path)
        storePly(ply_path, xyz, rgb)

    plydata = PlyData.read(ply_path)
    vertices = plydata['vertex']
    positions = np.vstack([vertices['x'], vertices['y'], vertices['z']]).T
    # normals = np.vstack([vertices['nx'], vertices['ny'], vertices['nz']]).T
    channels = dict(
        R=np.asarray(vertices['red']  , dtype=np.float32)/ 255,
        G=np.asarray(vertices['green'], dtype=np.float32)/ 255,
        B=np.asarray(vertices['blue'] , dtype=np.float32)/ 255,
    )
    point_cloud = PointCloud(coords=positions, channels=channels)

    return point_cloud
 
def intr_from_pinhole_camera(camera):
    if camera.model=='SIMPLE_PINHOLE':
        # https://github.com/colmap/colmap/blob/main/src/colmap/sensor/models.h#L250-L256
        f, cx, cy =  camera.params
        fx = fy = f
    elif camera.model=='PINHOLE':
        # https://github.com/colmap/colmap/blob/main/src/colmap/sensor/models.h#L265-L271
        fx, fy, cx, cy =  camera.params
    return np.array([[fx, 0, camera.width/2],
                     [0, fy, camera.height/2],
                     [0, 0, 1]])

def hw_from_pinhole_camera(camera):
    return [camera.height, camera.width]

def extr_from_image(image):
    R = image.qvec2rotmat()
    t = np.array(image.tvec)
    Rt = np.zeros((4, 4))
    Rt[:3, :3] = R.transpose()
    Rt[:3, 3] = t
    Rt[3, 3] = 1.0 
    return Rt

def read_camera_colmap(folder):
    """
    read camera from json file
    """
    max_depth = 1

    try:
        cameras_extrinsic_file = os.path.join(folder, "sparse", "images.txt")
        cam_extrinsics = read_extrinsics_text(cameras_extrinsic_file)
    except:
        cameras_extrinsic_file = os.path.join(folder, "sparse", "images.bin")
        cam_extrinsics = read_extrinsics_binary(cameras_extrinsic_file)

    try:
        cameras_intrinsic_file = os.path.join(folder, "sparse", "cameras.txt")
        cam_intrinsics = read_intrinsics_text(cameras_intrinsic_file)
    except:
        cameras_intrinsic_file = os.path.join(folder, "sparse", "cameras.bin")
        cam_intrinsics = read_intrinsics_binary(cameras_intrinsic_file)

    rgb_files = []
    depth_files = []
    poses = []
    intrinsics = []
    for k, v in cam_extrinsics.items():
        rgb_files.append(os.path.join(folder, "images", v.name))
        depth_path = os.path.join(folder, "stereo", "depth_maps" , v.name+".geometric.bin")
        if os.path.exists(depth_path): 
            depth_files.append(depth_path)
        c2w = extr_from_image(v)
        w2c_blender = np.linalg.inv(c2w)
        w2c_opencv = w2c_blender
        w2c_opencv[1:3] *= -1
        c2w_opencv = np.linalg.inv(w2c_opencv)
        poses.append(np.array(c2w_opencv))
        intrinsics.append(intr_from_pinhole_camera(cam_intrinsics[v.camera_id]))
    return rgb_files, poses, intrinsics, max_depth, depth_files

def read_all_colmap(folder):
    """
    read source images from a folder
    """
    # scene_src_dir = os.path.join(self.folder_path_src, scene_id)
    src_rgb_files, src_poses, src_intrinsics, max_depth, src_depth_files = read_camera_colmap(folder)

    src_cameras = []
    src_rgbs = []
    for src_rgb_file, src_pose, intrinsic in zip(src_rgb_files, src_poses, src_intrinsics):
        src_rgb , src_camera = read_image_colmap(src_rgb_file, src_pose, intrinsic)
        src_rgbs.append(src_rgb)
        src_cameras.append(src_camera)
    
    src_depths = []
    for src_depth_file in src_depth_files:
        src_depth = read_array(src_depth_file)
        src_depths.append(src_depth)

    src_rgbs = torch.stack(src_rgbs, axis=0)
    src_cameras = torch.stack(src_cameras, axis=0)
    src_alphas = torch.ones(src_rgbs.shape[:3]) # dumb init
    if src_depths:
        src_depths = torch.stack(src_depths, axis=0)
    else:
        src_depths = torch.ones(src_rgbs.shape[:3]) # dumb init
    
    return {
        "rgb": src_rgbs[..., :3],
        "camera": src_cameras,
        "depth": src_depths,
        "alpha": src_alphas,
    }

def read_image_colmap(rgb_file, pose, intrinsic_):
    rgb_np = imageio.imread(rgb_file).astype(np.float32) / 255.0
    rgb = torch.from_numpy(rgb_np)
    
    image_size = rgb.shape[:2]
    intrinsic = np.eye(4,4)
    intrinsic[:3,:3] = intrinsic_

    camera = torch.from_numpy(np.concatenate(
        (list(image_size), intrinsic.flatten(), pose.flatten())
    ).astype(np.float32))
    return rgb, camera

